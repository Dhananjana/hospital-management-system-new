    public void setData() {
        String a = empIDText.getText();
        if (a.length() > 0) {
            empID = Integer.parseInt(a);
        }
        Object ty = CBDate.getSelectedItem();
        if (ty != null) {
            String b = ty.toString();
            dte = Integer.parseInt(b);
        }
        Object ty1 = CBMonth.getSelectedItem();
        if (ty1 != null) {
            String c = ty1.toString();
            month = Integer.parseInt(c);
        }
        Object ty2 = CBYear.getSelectedItem();
        if (ty != null) {
            String c = ty2.toString();
            year = Integer.parseInt(c);
        }
        attend = 0;
        if (yesBtn.isSelected()) {
            attend = 1;
        }
        if (noBtn.isSelected()) {
            attend = 0;
        }
    }

