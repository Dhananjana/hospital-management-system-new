    private void empIDTextFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_empIDTextFocusLost
        String ID = empIDText.getText();
        int empID = 0;
        if (ID.length() > 0) {
            empID = Integer.parseInt(ID);
        }
        try {
            connection c = new connection();
            Connection cn = c.getConnect();
            Statement st = cn.createStatement();
            if (empID > 0) {
                ResultSet rs = st.executeQuery("select * from employee where empID ='" + empID + "'");
                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "NO match result in Employee table!");
                    empIDText.setText("");
                }
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_empIDTextFocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            connection c = new connection();
            Connection cn = c.getConnect();
            Statement st = cn.createStatement();

            this.setData();
            
            if (empID > 0) {
                ResultSet rs = st.executeQuery("select * from getAttendance where empID='" + this.empID + "' and month = '" + this.month + "'");
                if (rs.next()) {
                    int d = rs.getInt("date");
                    
                    if (this.dte == d) {
                        JOptionPane.showMessageDialog(null, "already exist!");
                    } else if (attend == 1) {
                        
                        st.executeUpdate("insert into getAttendance(empID, date, month, year, attend) values('" + empID + "','" + dte + "','" + month + "','" + year + "','" + attend + "')");
                        JOptionPane.showMessageDialog(null, "succesfully added!");
                        this.reset();
                    }
                    else{
                        st.executeUpdate("insert into getAttendance(empID, date, month, year, attend) values('" + empID + "','" + dte + "','" + month + "','" + year + "','" + attend + "')");
                        JOptionPane.showMessageDialog(null, "succesfully added!");
                        this.reset();
                        
                    }

                } else {
                    st.executeUpdate("insert into getAttendance(empID, date, month, year, attend) values('" + empID + "','" + dte + "','" + month + "','" + year + "','" + attend + "')");
                    JOptionPane.showMessageDialog(null, "succesfully added!");
                    this.reset();
                }
            } else {
                JOptionPane.showMessageDialog(null, "fill empid! ");
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

