    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        try {
            connection c = new connection();//create an object of connection class
            Connection cn = c.getConnect();//make connection
            Statement st = cn.createStatement();

            this.setData();
            //take resultset from doctor tabel where employee ID exist
            ResultSet rs = st.executeQuery("select * from doctor where empID='" + this.empID + "'");
            if (rs.next()) {
                st.executeUpdate("delete from doctor where empID='" + this.empID + "'");
                st.executeUpdate("delete from employee where empID='" + this.empID + "'");
                JOptionPane.showMessageDialog(this, "Successfully deleted");
                this.reset();
                //if resultset exist delete from doctor and also from employee tables.

            } else {
                //if there wasn't a resultset show error message.
                JOptionPane.showMessageDialog(this, "No result to delete");

            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_delBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
            connection c = new connection();//create an object of connection class
            Connection cn = c.getConnect();
            Statement st = cn.createStatement();

            this.setData();
            if (empID > 0) {

                ResultSet rs = st.executeQuery("select * from doctor where empID='" + this.empID + "'");//take resultset from doctor table.
                if (rs.next()) {
                    uniText.setText(rs.getString("university"));//take the value from resultset and add it show it in form.
                    subText.setText(rs.getString("subject"));
                } else {
                    JOptionPane.showMessageDialog(this, "No search result found");//show error message when there wasn't a resultset.
                }
            } else {
                JOptionPane.showMessageDialog(null, "Add employee number!");
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

