    private void empIDTextFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_empIDTextFocusLost
        String cid = empIDText.getText();//take employee id field value
        int cd = 0;
        if (cid.length() > 0) {
            cd = Integer.parseInt(cid);//if that textfield not empty add to int variable
        }
        try {
            connection c = new connection();//create an object of connection class
            Connection cn = c.getConnect();//create a connection
            Statement st = cn.createStatement();
            if (cd > 0) {
                ResultSet rs = st.executeQuery("select * from employee where empID='" + cd + "'");//check whether that employee exist in employee table
                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Invalid doctor ID!");//if there wasn't a value show error message
                    empIDText.setText("");
                } else {
                    String type = rs.getString("empType");
                    char[] arr = type.toCharArray();

                    String d = "Doctor";
                    char[] arr1 = d.toCharArray();

                    boolean val = Arrays.equals(arr, arr1);//check whehther that existing result set representing a doctor details

                    if (val == false) {
                        JOptionPane.showMessageDialog(this, "Invalid doctor ID!");//if it not a doctor detail show error message.
                        empIDText.setText("");
                    }
                }

            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }

