    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        try {
            connection c = new connection(); //create an object of connection class
            Connection c1 = c.getConnect(); //create connection by using that connection
            Statement st = c1.createStatement(); //create statement type object

            this.setData();
            //take all lengths of inputs 
            int l = (int) Math.log10(empID) + 1;
            int l2 = university.length();
            int l3 = special.length();

            //take result set from doctor table which equal to employee ID given by user
            ResultSet rs = st.executeQuery("select * from doctor where empID='" + empID + "' ");

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "empID already exist");//if the resultset is already exist means we cannot add new doctor.
                this.reset(); //so reset all details
            } else {
                String ty = "Doctor";
                Statement st2 = c1.createStatement();
                //take the resultset from employee table where employee id and field doctor.
                ResultSet rs2 = st2.executeQuery("select * from employee where empID='" + this.empID + "' and empType='" + ty + "'");
                if (rs2.next()) {
                    //if that result set exist check whether that length of the user match with the database length of variables
                    if (l < 6 || l2 < 31 || l3 < 41) {
                        //if length are okay add details to the doctor table and update it.
                        st.executeUpdate("insert into doctor(empID, university, subject) values('" + empID + "','" + university + "', '" + special + "')");
                        this.reset();
                    } else {
                        //if there input size more than the size where database variable size show error massage
                        JOptionPane.showMessageDialog(this, "input size error");
                    }
                } else {
                    //if there wasn't details in employee table remember user to register doctor in employee form.
                    JOptionPane.showMessageDialog(this, "First add Details in employee");
                }
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_registerBtnActionPerformed

